<template>
  <base-table
    ref="table"
    name="rule"
    :loading="$apollo.queries.rules.loading"
    :data="rules"
    :columns="columns"
    @create="createRule"
    @clone="cloneRule"
    @delete="deleteRule"
  >
    <template #body-cell-zone="{value}">
      <div class="q-gutter-sm row">
        <zone-link :name="value" />
      </div>
    </template>
    <template #body-cell-enabled="{value}">
      <q-toggle
        :value="value"
        :color="value ? 'positive' : 'negative'"
        keep-color
      />
    </template>
    <!-- 
    <template v-slot:details="{ row }">
      <RuleDetails :rule="row" />
    </template>
    <template v-slot:popup="{ row, closePopup }">
      <CreateRule :value="row" @close="closePopup" :edit="false" />
    </template> -->
  </base-table>
</template>

<script>
import db from "src/gql";
import BaseTable from "../../components/BaseTable3.vue";
//import RuleDetails from "src/components/DNS/Rule/Details.vue";
import ZoneLink from "src/components/DNS/Zone/Link.vue";
import ZoneDialog from "src/components/DNS/Rule/Dialog.vue";

export default {
  components: { BaseTable, ZoneLink },
  apollo: {
    rules: {
      query: db.dns.GET_RULES,
      update: data => data.dnsRules
    }
  },
  created() {
    this.$root.$on("refresh", () => this.$apollo.queries.rules.refetch());
  },
  data() {
    const field = (name, opt = {}) => ({
      name: name,
      align: "left",
      label: name,
      field: name,
      sortable: true,
      ...opt
    });

    const columns = [
      field("zone"),
      field("name"),
      field("type"),
      field("ttl", { label: "TTL" }),
      field("records", {
        format: r => r.map(r => r.content).join(", ")
      }),
      field("enabled", { field: r => this.isEnabled(r) })
    ];
    return { columns };
  },
  methods: {
    createRule() {
      this.$q.dialog({
        component: ZoneDialog,
        parent: this
      });
    },
    cloneRule(rule) {
      this.$q.dialog({
        component: ZoneDialog,
        parent: this,
        rule
      });
    },
    deleteRule(rule) {
      this.$q
        .dialog({
          title: "Confirm",
          message: `Are you sure you want to delete (${rule.type}) ${rule.name}?`,
          color: "negative",
          cancel: true
        })
        .onOk(() => {
          this.$apollo.mutate({
            mutation: db.dns.DELETE_RULE,
            variables: { nodeId: rule.nodeId },
            refetchQueries: [{ query: db.dns.GET_RULES }]
          });
        });
    },
    isEnabled(r) {
      const enabled = r["records"].reduce((p, c) => ~~c["enabled"] + p, 0);
      if (enabled === r["records"].length) {
        return true;
      }
      return enabled === 0 ? false : "partial";
    }
  }
};
</script>
